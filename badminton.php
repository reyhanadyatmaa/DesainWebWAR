  <p>Bulu tangkis adalah suatu olahraga rkt yang dimainkan oleh dua orang (untuk tunggal) atau dua pasangan (untuk ganda) yang saling berlawanan. Olah raga yang satu ini hampir mirip seperti tenis. Secara harfiah bulu tangkis berasal dari dua kata, yaitu bulu dan tangkis. Kata bulu diambil dari bentuk atau wujud kok (shuttlecock) yang terbuat dri bulu angsa. Sedangkan “tangkis” diambil dari kata dasar menangkis. Inti dari permainan bulu tangkis adalah menangkis pergerakan dari shuttlecock (bulu) tersebut. Perihal awal mula dan asal permainan bulu tangkis, berbagai pendapat muncul. Pendapat pertama menjelaskan bahwa permainan ini berasal dan berkembang di Mesir kuno pada 2000 tahun yang lalu. Pendapat selanjutnya mengatakan bahwa permainan ini berasal dari India dan daratan Tiongkok. Berkembangnya permainan bulu tangkis ini dikenal luas oleh msyarakat dunia adalah pada abad pertengahan di Inggris, yaitu dari sebuah permainan tradisional anak-anak setempat yang bernama Battledore dan Shuttlecocks. Permainan tradisional ini sangat populer di kalangan masyarakat hingga pada akhirnya sebuah majalah bernama “Punch” mempublikasikan permainan ini. Tujuan permainan bulu tangkis adalah mengumpulkan angka sampai 21. Pemain yang telah lebih dulu mengumpulkan poin atau angka hingga 21 adalah pemenangnya. Pemain bulu tangkis dibedakan menjadi paratai tunggal dan partai ganda. Partai tersebut dapat dimainkan oleh putra maupun putri. Selain itu, partai ganda juga dapat dimainkan secara ganda campuran. Peraturan yang digunakan daalam permainan bulu tangkis harus sesuai dengan peraturan IBF. Kebijakan ini tidak hanya berlaku bagi perlengkapan dan peralatan yang digunakan saja, melainkan partai yang berkepentingan juga harus mengikutinya. Misalnya produsen peralatan.</p>
